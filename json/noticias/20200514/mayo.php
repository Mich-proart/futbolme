<H1 CLASS="western"><SPAN STYLE="background: #ffffff">Madrid y Barça:
su pugna por el título de Liga... ¿Y por Mbappé?</SPAN></H1>
<P STYLE="margin-bottom: 0.11in">
	<div class="text-center"><IMG src="json/noticias/20200514/imagen.jpg" NAME="Imagen 1" ALIGN="BOTTOM" WIDTH="567" HEIGHT="353" BORDER="0"></div></P>
<P STYLE="margin-bottom: 0.11in"><BR><BR>
</P>
<P STYLE="margin-bottom: 0.11in"><SPAN STYLE="background: #ffffff">Tan
solo dos puntos de diferencia separan al Real Madrid y el Barcelona,
que una vez reanuden su lucha por conseguir el título de Liga (lo
cual se espera que ocurra a mediados del mes de junio), tendrán </SPAN><B><SPAN STYLE="background: #ffffff">once
jornadas a las que hacer frente</SPAN></B><SPAN STYLE="background: #ffffff">
por delante.</SPAN></P>
<P STYLE="margin-bottom: 0.11in"><SPAN STYLE="background: #ffffff">Los
blancos cedieron su liderato justo en la última jornada que
disputaron antes de que se iniciara el parón. <A HREF="https://www.20minutos.es/deportes/noticia/4180089/0/betis-real-madrid-directo/">El
conjunto de Zidane se dejó los tres puntos en el Villamarín</A>, lo
que permitió al Barça colocarse por delante con dos puntos y
gracias a su victoria frente a la Real Sociedad.</SPAN></P>
<P STYLE="margin-bottom: 0.11in"><SPAN STYLE="background: #ffffff">El
equipo madridista tiene ahora que volver a conseguir el liderato en
las próximas jornadas que le quedan, con seis encuentros como
locales en el Alfredo Di Stefano, y cinco que jugarán como
visitantes. Por su parte, los blaugrana tienen cinco encuentros
dentro del Camp Nou, y seis fuera del estadio.</SPAN></P>
<P STYLE="margin-bottom: 0.11in"><BR><BR>
</P>
<H2 CLASS="western"><SPAN STYLE="background: #ffffff">¿Cuáles son
los encuentros clave que le quedan a cada equipo?</SPAN></H2>
<P STYLE="margin-bottom: 0.11in; background: #ffffff"><SPAN STYLE="background: #ffffff">Sin
duda, los duelos más difíciles que tiene que jugar el Real Madrid
aún son los de </SPAN><B><SPAN STYLE="background: #ffffff">San Mamés
y Anoeta, </SPAN></B><SPAN STYLE="background: #ffffff">correspondientes
a las jornadas 34 y 30 respectivamente. No se queda atrás tampoco el
encuentro en casa, en la jornada 29, </SPAN><B><SPAN STYLE="background: #ffffff">frente
al Valencia.</SPAN></B></P>
<P STYLE="margin-bottom: 0.11in"><SPAN STYLE="background: #ffffff">El
Barcelona tampoco lo tiene nada fácil, ya que aún debe recibir en
casa al </SPAN><B><SPAN STYLE="background: #ffffff">Atlético de
Madrid y el Athletic Club,</SPAN></B><SPAN STYLE="background: #ffffff">
en la jornada 31 el primero y en la 33 el segundo. También tiene una
visita complicada al </SPAN><B><SPAN STYLE="background: #ffffff">Sánchez
Pizjuán</SPAN></B><SPAN STYLE="background: #ffffff"> en la jornada
30. </SPAN>
</P>
<P STYLE="margin-bottom: 0.11in"><SPAN STYLE="background: #ffffff">Al
igual que el conjunto merengue, también los culés tienen su última
jornada fuera de casa. Con un panorama como este, parece que cuando
se reanude la competición será de lo más emocionante para la
afición y las <A HREF="https://www.888sport.es/">apuestas deportivas
sobre La Liga</A>.</SPAN></P>
<H2 CLASS="western"><SPAN STYLE="background: #ffffff">Mbappé parece
objeto de deseo para blancos y azulgranas</SPAN></H2>
<P STYLE="margin-bottom: 0.11in">Hay una noticia que une a Real
Madrid y el Fútbol Club Barcelona estos días, que tiene que ver con
Kylian Mbappé. De todos es sabido que el delantero francés <B>es el
gran objetivo de Florentino Pérez, </B>ya que desea que se convierta
en el gran próximo fichaje galáctico. El propio jugador ha enviado
ya varios mensajes a Zidane.</P>
<P STYLE="margin-bottom: 0.11in">La cuestión es que el Barcelona ha
mandado ahora un mensaje que pone sobre aviso a los blancos. Ha sido
<B>Víctor Font</B> quien se ha pronunciado sobre este tema, al
declarar lo siguiente en un encuentro con los aficionados del Barça:
&quot;Soy más de Kylian Mbappé que de Neymar, que es más creativo.
El francés me parece más centrado en el fútbol&quot;.</P>
<P STYLE="margin-bottom: 0.11in">Estas afirmaciones chocan
frontalmente con los planes del Real Madrid, ya que Florentino
pretende que Mbappé no renueve su actual contrato con el Paris
Saint-Germain, que finaliza en 2022, para proceder a su fichaje este
verano.</P>
<P STYLE="margin-bottom: 0.11in">Pero, ¿qué tiene que decir Mbappé
de todo esto? Lo cierto es que <B>el futbolista no ha ocultado nunca
su intención de ponerse bajo las órdenes de Zidane,</B> y su precio
podría estar en torno a los 200 millones de euros. El hecho de que
el Real Madrid mantenga una relación cordial con el PSG podría
acelerar las negociaciones.</P>
